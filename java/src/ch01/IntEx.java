package ch01;

public class IntEx {
	// class는 변수와 함수의 집합체
	
	public static void main(String[] args) {
		// void는 계산만 하는 함수로 돌려주는 것이 없음
		// (String[] args)와 같이 소괄호 안에 들어가는 것이 인자arguments
		// 대괄호는 배열array을 표현할 때 쓰는 것
		
		int a = 100;
		System.out.println(a);
		// System 클래스의 안에 들어있는 out이라는 기능을 사용하고 그 안의 print 함수를 이용한다는 뜻
		
		String str = "Hello";
		System.out.println(str);
		
		char c = 'C' ;
		System.out.println(c);
		// 문자열(string)이 아니라 딱 한 글자만 필요할 때 char를 자료형으로 하고 작은따옴표를 쓴다
		
		// 실행은 Ctrl F11
		

	}

}
