package ch02;

public class Assign {

	public static void main(String[] args) {

		int a = 10, b = 20;
		a += 5;
		// a = a + 5
		System.out.println(a);

		a -= 5;
		// a = a - 5
		System.out.println(a);

		b *= 5;
		// b = b * 5
		System.out.println(b);

		b /= 5;
		// b = b / 5
		System.out.println(b);

	}

}
